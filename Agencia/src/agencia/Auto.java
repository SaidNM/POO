
package agencia;


public class Auto {
    private final String marca;
    private final String modelo;
    private String placas;

    public Auto(String marca, String modelo, String placas) {
        this.marca = marca;
        this.modelo = modelo;
        this.placas = placas;
    }

    public void setPlacas(String placas) {
        this.placas = placas;
    }
    
    
    
    
}
